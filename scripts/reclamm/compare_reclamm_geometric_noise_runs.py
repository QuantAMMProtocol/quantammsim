"""Compare two geometric reCLAMM runs against matched arb-only baselines.

This script reuses the same AAVE/ETH reCLAMM fingerprint and parameter wiring as
``compare_reclamm_thermostats.py``, but runs only the geometric interpolation
mode and plots:
1. Share price / TVL over time in absolute USD terms
2. Pool weights over time
3. Estimated gross swap volume over time
4. Noise-model improvement over arb-only over time

Because these runs use no LP supply changes, share price and TVL are the same
series here.

Usage:
    python scripts/reclamm/compare_reclamm_geometric_noise_runs.py
    python scripts/reclamm/compare_reclamm_geometric_noise_runs.py \
        --adjacent-csv scripts/results/...csv --adjacent-row-index 0
"""

